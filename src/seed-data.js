export function youtubeIdFromUrl(url) {
  try {
    const u = new URL(url);
    if (u.hostname.includes('youtu.be')) return u.pathname.replace(/^\//, '').split('/')[0];
    const v = u.searchParams.get('v');
    if (v) return v;
  } catch {
    /* ignore */
  }
  const m = url.match(/(?:youtu\.be\/|v=)([a-zA-Z0-9_-]{6,})/);
  return m ? m[1] : '';
}

