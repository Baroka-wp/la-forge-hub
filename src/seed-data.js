export function parseSessions(raw = RAW_SESSIONS) {
  return raw
    .trim()
    .split('\n')
    .map((line) => {
      const [date, title, url, tag] = line.split('|');
      const d = new Date(date);
      const monthKey = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`;
      const day = d.getDate();
      const weekday = d.toLocaleDateString('fr-FR', { weekday: 'short' }).replace('.', '');
      const youtubeId = youtubeIdFromUrl(url);
      return {
        date: d,
        monthKey,
        day,
        weekday,
        title,
        url,
        tag,
        youtubeId,
        lessonId: youtubeId,
      };
    });
}
